# n5_master.xlsx 생성
# ────────────────────────────────────────
def create_master_xlsx():
    wb = Workbook()

    # ── Sheet 1: master_list ──
    ws1 = wb.active
    ws1.title = "master_list"
    headers = ["grammar_point_id","chunk_type","japanese_name","korean_meaning",
               "level","l1","l2","source_pool","frequency","note"]
    style_header(ws1, headers)
    for row_idx, item in enumerate(MASTER_LIST, 2):
        gid, jname, kmean, l1, l2, src, freq = item
        is_border = gid in BORDER_IDS
        is_new = gid in {"grammar_n5_106","grammar_n5_107","grammar_n5_108","grammar_n5_109","grammar_n5_110"}
        note = ""
        if is_new:
            note = "E-12 신규"
        elif gid in {"grammar_n5_079","grammar_n5_080"}:
            note = "E-2: L1-7→L1-9 이관"
        row = [gid,"point",jname,kmean,"N5",l1,l2,src,freq,note]
        for col_idx, val in enumerate(row, 1):
            ws1.cell(row=row_idx, column=col_idx, value=val)
        style_data_row(ws1, row_idx, len(headers), is_border, is_new)
    auto_col_width(ws1)
    ws1.row_dimensions[1].height = 20

    # ── Sheet 2: taxonomy_def ──
    ws2 = wb.create_sheet("taxonomy_def")
    t_headers = ["l1_name","l2_name","definition","item_count","id_range_examples"]
    style_header(ws2, t_headers)
    for row_idx, item in enumerate(TAXONOMY_DEF, 2):
        for col_idx, val in enumerate(item, 1):
            ws2.cell(row=row_idx, column=col_idx, value=val)
        style_data_row(ws2, row_idx, len(t_headers))
    auto_col_width(ws2)

    # ── Sheet 3: l3_assignment ──
    ws3 = wb.create_sheet("l3_assignment")
    l3_headers = ["grammar_point_id","border_candidate","border_reason",
                  "connection_type","tense","polarity","formality",
                  "comparison_pair_ids","embedding_text"]
    style_header(ws3, l3_headers)
    for row_idx, item in enumerate(MASTER_LIST, 2):
        gid = item[0]
        is_border = gid in BORDER_IDS or gid in E12_BORDER_IDS
        l3 = L3_DATA.get(gid, (None,"any","both","both","[]",""))
        conn, tense, pol, form, comp_pair, emb = l3
        border_reason = ""
        for bid, reason, _, _ in BORDER_META:
            if bid == gid:
                border_reason = reason
                break
        row = [gid, is_border, border_reason, conn, tense, pol, form, comp_pair, emb]
        for col_idx, val in enumerate(row, 1):
            ws3.cell(row=row_idx, column=col_idx, value=val)
        style_data_row(ws3, row_idx, len(l3_headers), is_border, gid in {"grammar_n5_106","grammar_n5_107","grammar_n5_108","grammar_n5_109","grammar_n5_110"})
    auto_col_width(ws3)

    # ── Sheet 4: border_meta ──
    ws4 = wb.create_sheet("border_meta")
    b_headers = ["grammar_point_id","border_reason","n5_scope","n4_advanced"]
    style_header(ws4, b_headers)
    for row_idx, item in enumerate(BORDER_META, 2):
        for col_idx, val in enumerate(item, 1):
            ws4.cell(row=row_idx, column=col_idx, value=val)
        style_data_row(ws4, row_idx, len(b_headers), is_border=True)
    auto_col_width(ws4)

    # ── Sheet 5: variant_chunks ──
    ws5 = wb.create_sheet("variant_chunks")
    v_headers = ["grammar_point_id","chunk_type","base_id","japanese_name","korean_meaning",
                 "level","l1","l2","variant_label","border_candidate","embedding_text","note"]
    style_header(ws5, v_headers)
    for row_idx, vc in enumerate(VARIANT_CHUNKS, 2):
        row = [
            vc["grammar_point_id"], vc["chunk_type"], vc["base_id"],
            vc["japanese_name"], vc["korean_meaning"], vc["level"],
            vc["l1"], vc["l2"], vc["variant_label"],
            str(vc["border_candidate"]) if vc["border_candidate"] is not None else "미결",
            vc["embedding_text"], vc["note"],
        ]
        for col_idx, val in enumerate(row, 1):
            ws5.cell(row=row_idx, column=col_idx, value=val)
        style_data_row(ws5, row_idx, len(v_headers))
    auto_col_width(ws5)

    out_path = OUTPUT_DIR / "n5_master.xlsx"
    wb.save(out_path)
    print(f"✓ n5_master.xlsx 저장: {out_path}")
    print(f"  - master_list: {len(MASTER_LIST)} rows")
    print(f"  - border_meta: {len(BORDER_META)} rows")
    print(f"  - variant_chunks: {len(VARIANT_CHUNKS)} rows")


