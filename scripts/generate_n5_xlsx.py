def style_header(ws, headers, row=1):
    for col_idx, h in enumerate(headers, 1):
        cell = ws.cell(row=row, column=col_idx, value=h)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.alignment = CENTER
        cell.border = THIN_BORDER


def style_data_row(ws, row_idx, ncols, is_border=False, is_new=False):
    fill = BORDER_FILL if is_border else (NEW_FILL if is_new else None)
    for c in range(1, ncols + 1):
        cell = ws.cell(row=row_idx, column=c)
        cell.alignment = LEFT
        cell.border = THIN_BORDER
        if fill:
            cell.fill = fill


def auto_col_width(ws, min_w=8, max_w=50):
    for col in ws.columns:
        w = max(len(str(cell.value or "")) for cell in col)
        ws.column_dimensions[get_column_letter(col[0].column)].width = max(min_w, min(w + 2, max_w))


